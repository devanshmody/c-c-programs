#include<stdio.h>
#include<conio.h>
#include<graphics.h>
#include<math.h>
void main()
{

float x,y,x1,y1,x2,y2,e,dx,dy,length,s1,s2;
int i,k,exc,gd,gm;
clrscr();
detectgraph(&gd,&gm);
initgraph(&gd,&gm,"");
for(k=0;k<6;k++)
{
printf(" x1\t,y1\t,x2\t,y2");
scanf("%f%f%f%f",&x1,&y1,&x2,&y2);
dx=abs(x2-x1);
dy=abs(y2-y1);
x=x1;
y=y1;
if(x2<x1)
{
s1=-1;
}
else if(x2>x1)
{
s1=1;
}
else
{
s1=0;
}
if(y2<y1)
{
s2=-1;
}
else
{
s2=1;
}
if(dy>dx)
{
dx=dy;
exc=1;
}
else
{
dy=dx;
exc=0;
}
e=2*dy-dx;
i=1;
do
{
putpixel(x,y,10);
while(e>=0)
{
if(exc==1)
{
x=x+s1;
}
else
{
y=y+s2;
}
e=e-2*dx;
}
while(e<0)
{
if(exc==1)
{
y=y+s2;
}
else
{
x=x+s1;
}
e=e+2*dy;
}
i=i+1;
}
while(i<=dx);
}
getch();
closegraph();
}